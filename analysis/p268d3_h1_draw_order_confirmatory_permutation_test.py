def prepare_game_records(all_records: list[dict], game: str) -> dict:
    """Filter to a single game, sort chronologically, exclude EXCLUDED_MONTHS,
    and split chronologically into estimation (70%) / holdout (30%).

    The holdout slice is computed for bookkeeping/reporting only (record
    counts) and its contents are NOT used by any statistic in this task.
    """
    recs = [r for r in all_records if r["lottery_type"] == game]
    recs.sort(key=lambda r: r["draw_date"])
    eligible = [r for r in recs if r["month"] not in EXCLUDED_MONTHS]
    excluded = [r for r in recs if r["month"] in EXCLUDED_MONTHS]

    n_eligible = len(eligible)
    n_estimation = int(n_eligible * ESTIMATION_FRACTION)
    estimation = eligible[:n_estimation]
    holdout = eligible[n_estimation:]

    return {
        "game": game,
        "total_records": len(recs),
        "excluded_records": len(excluded),
        "eligible_records": n_eligible,
        "estimation_records": len(estimation),
        "holdout_records": len(holdout),
        "estimation_date_range": (
            [estimation[0]["draw_date"], estimation[-1]["draw_date"]]
            if estimation
            else None
        ),
        "holdout_date_range": (
            [holdout[0]["draw_date"], holdout[-1]["draw_date"]] if holdout else None
        ),
        "estimation_records_data": estimation,
    }


def extract_balls_matrix(records: list[dict], cfg: dict) -> np.ndarray:
    k = cfg["slice_len"]
    rows = []
    for r in records:
        seq = r["drawNumberAppear"]
        rows.append(seq[:k])
    return np.array(rows, dtype=np.int64)


def statistic_from_ranks(balls_flat: np.ndarray, ranks_flat: np.ndarray, cfg: dict) -> float:
    pool_min = cfg["pool_min"]
    pool_max = cfg["pool_max"]
    k = cfg["k"]
    pool_size = pool_max - pool_min + 1

    ids = balls_flat - pool_min
    sums = np.bincount(ids, weights=ranks_flat.astype(np.float64), minlength=pool_size)
    counts = np.bincount(ids, minlength=pool_size)

    mask = counts > 0
    means = sums[mask] / counts[mask]
    mu_k = (k + 1) / 2.0
    var_k = (k * k - 1) / 12.0
    z = (means - mu_k) / np.sqrt(var_k / counts[mask])
    return float(np.sum(z * z))


def run_h1_for_game(game: str, estimation_records: list[dict], cfg: dict, n_permutations: int, seed: int) -> dict:
    balls = extract_balls_matrix(estimation_records, cfg)
    m, k = balls.shape
    assert k == cfg["k"]

    ranks_obs = np.tile(np.arange(1, k + 1), (m, 1))
    t_obs = statistic_from_ranks(balls.flatten(), ranks_obs.flatten(), cfg)

    rng = np.random.default_rng(seed)
    null_stats = np.empty(n_permutations, dtype=np.float64)
    balls_flat = balls.flatten()
    start = time.monotonic()
    for i in range(n_permutations):
        rand_keys = rng.random((m, k))
        order = np.argsort(rand_keys, axis=1)
        ranks_perm = np.argsort(order, axis=1) + 1
        null_stats[i] = statistic_from_ranks(balls_flat, ranks_perm.flatten(), cfg)
    elapsed = time.monotonic() - start

    n_ge = int(np.sum(null_stats >= t_obs))
    p_value = (1 + n_ge) / (n_permutations + 1)

    return {
        "game": game,
        "role": cfg["role"],
        "pool_min": cfg["pool_min"],
        "pool_max": cfg["pool_max"],
        "pool_size": cfg["pool_max"] - cfg["pool_min"] + 1,
        "k": k,
        "n_draws_estimation": m,
        "observed_statistic": t_obs,
        "null_distribution_summary": {
            "n_permutations": n_permutations,
            "mean": float(np.mean(null_stats)),
            "std": float(np.std(null_stats)),
            "min": float(np.min(null_stats)),
            "max": float(np.max(null_stats)),
            "p50": float(np.percentile(null_stats, 50)),
            "p95": float(np.percentile(null_stats, 95)),
            "p99": float(np.percentile(null_stats, 99)),
        },
        "p_value_one_sided": p_value,
        "seed": seed,
        "elapsed_seconds": elapsed,
    }


