"""
P268D-3: H1 confirmatory permutation test for per-ball exit-rank heterogeneity
in the official `drawNumberAppear` (draw-order) field.

Hard constraints (per P268C/P268D1 registry-freeze design and this task's
authorization):
  - This task runs H1 ONLY. H2 and H3 are NOT executed here.
  - Data source is the P268D-1 full-history JSONL artifact
    (`outputs/research/p268d1_draw_order_full_history_artifact_backfill_20260610.jsonl`),
    NOT the local SQLite DB. No DB connection of any kind is opened.
  - Before any H1 computation, this script appends exactly ONE pre-registration
    entry to the production Hypothesis Registry
    (`lottery_api/data/hypothesis_registry.jsonl`), id
    `HR-P268D3-H1-DRAW-ORDER-EXIT-RANK-001`, status `PRE_REGISTERED_BEFORE_TEST`.
  - 2026-04 and 2026-05 are excluded from the eligible draw set (already
    inspected by P268B; excluded from any confirmatory window per P268D1
    registry-freeze).
  - Eligible draws (per lottery_type) are split chronologically 70%
    estimation / 30% holdout. Only the estimation window is used by H1 in
    this task; the holdout window is NOT opened, NOT read, and NOT used for
    any statistic in this task (reserved for H1_holdout in a future task).
  - Null model: within-draw permutation null. For each draw, the `k`
    drawn-ball identities (a fixed-size slice of `drawNumberAppear`) are
    treated as an exchangeable set; under the null, all k! orderings of
    that set are equally likely. The observed test statistic is compared
    against a Monte Carlo null distribution generated by independently
    re-permuting each draw's order labels.
  - Test statistic: dispersion (sum of squared standardized per-ball mean
    exit-rank) across all balls in the relevant pool. "Exit rank" = 1-based
    position of a ball within the (sliced) `drawNumberAppear` sequence.
  - DAILY_539 is the primary game (alpha = 0.01, single pre-registered test,
    no cross-game correction needed for H1 itself per the registry-freeze).
    BIG_LOTTO / POWER_LOTTO / 3_STAR / 4_STAR are computed and reported but
    are explicitly labeled SECONDARY / EXPLORATORY ONLY and cannot alone
    promote to a strategy or change the H1 PASS/FAIL classification.
  - No DB write of any kind. No new strategy. No hit-rate / success-rate
    improvement claim of any kind.
"""

