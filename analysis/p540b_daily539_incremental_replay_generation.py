def write_artifacts(result: dict[str, Any], out_json: Path, out_md: Path) -> None:
    if out_json.exists():
        raise FileExistsError(f"refusing to overwrite existing artifact: {out_json}")
    if out_md.exists():
        raise FileExistsError(f"refusing to overwrite existing artifact: {out_md}")
    out_json.parent.mkdir(parents=True, exist_ok=True)
    out_json.write_text(json.dumps(result, ensure_ascii=False, indent=2, default=str) + "\n", encoding="utf-8")
    out_md.write_text(render_markdown(result) + "\n", encoding="utf-8")


def _default_dated_paths() -> tuple[Path, Path]:
    stamp = datetime.now(timezone.utc).strftime("%Y%m%d")
    json_path = OUTPUT_DIR / f"p540b_daily539_incremental_replay_generation_{stamp}.json"
    md_path = OUTPUT_DIR / f"p540b_daily539_incremental_replay_generation_{stamp}.md"
    return json_path, md_path


def main(argv: list[str] | None = None) -> int:
    default_json, default_md = _default_dated_paths()
    parser = argparse.ArgumentParser(description="P540B DAILY_539 incremental replay generation")
    parser.add_argument("--db", default=str(DEFAULT_DB_PATH))
    parser.add_argument("--mode", choices=["manifest", "dry-run", "apply"], default="dry-run")
    parser.add_argument("--out-json", default=str(default_json))
    parser.add_argument("--out-md", default=str(default_md))
    args = parser.parse_args(argv)

    if args.mode == "manifest":
        conn = _open_ro(args.db)
        try:
            pre_snapshot = build_pre_write_snapshot(conn)
            manifest = build_manifest(pre_snapshot, _build_adapters())
        finally:
            conn.close()
        print(json.dumps(manifest, indent=2, ensure_ascii=False))
        return 0

    if args.mode == "dry-run":
        preview = build_dry_run_preview(args.db)
        print(
            json.dumps(
                {
                    "manifest_consistent": preview["manifest_consistent"],
                    "row_count": preview.get("row_count"),
                    "matches_manifest_expected_count": preview.get("matches_manifest_expected_count"),
                    "predicted_count": preview.get("predicted_count"),
                    "error_count": preview.get("error_count"),
                },
                indent=2,
            )
        )
        return 0

    # apply
    result = run_write(args.db)
    write_artifacts(result, Path(args.out_json), Path(args.out_md))
    print(json.dumps({"classification": result["classification"], "out_json": args.out_json}, ensure_ascii=False))
    return 0 if result["all_invariants_pass"] else 1


if __name__ == "__main__":
    raise SystemExit(main())
