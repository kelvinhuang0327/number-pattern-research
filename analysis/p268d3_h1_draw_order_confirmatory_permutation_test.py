# Per-game ball-pool / draw-order configuration for the H1 exit-rank statistic.
#
#   pool_min/pool_max: inclusive range of distinct ball identities considered
#                       for the within-draw permutation (a single namespace).
#   k:                  number of positions in the sliced drawNumberAppear
#                       sequence used for the H1 statistic (the "drawn set").
#   slice_len:          number of leading elements of drawNumberAppear used.
#
# DAILY_539: all 5 drawn numbers, single pool 1-39 (no special number).
# BIG_LOTTO: all 7 positions (6 main + 1 special), single pool 1-49 -- the
#            special number is drawn from the same 1-49 pool as the main
#            numbers, so a single 7-ball within-draw permutation namespace
#            is well-defined.
# POWER_LOTTO: only the first 6 (main) positions, pool 1-38. The 7th
#            (special) position is drawn from a disjoint, smaller pool
#            (1-8) and is EXCLUDED to avoid mixing two incompatible ball
#            namespaces under one permutation.
# 3_STAR / 4_STAR: digit positions, pool 0-9, k = 3 / 4. Digits may repeat
#            within a draw; the within-draw permutation null is still
#            well-defined as a permutation of the (possibly repeated)
#            multiset of digit identities.
