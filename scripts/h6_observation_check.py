def _exit_code(verdict: str) -> int:
    return {"OK": 0, "WATCH": 1, "ACTION_REQUIRED": 2}.get(verdict, 2)


def _print_human(result: dict) -> None:
    ss = result["strategy_state"]
    lm = result["live_metrics"]
    lr = result["latest_report"]
    dh = result["data_hygiene"]
    sh = result["scheduler_health"]
    verdict = result["observation_verdict"]
    reasons = result["verdict_reasons"]

    RESET = "\033[0m"
    BOLD = "\033[1m"
    GREEN = "\033[32m"
    YELLOW = "\033[33m"
    RED = "\033[31m"
    verdict_color = {"OK": GREEN, "WATCH": YELLOW, "ACTION_REQUIRED": RED}.get(verdict, RESET)

    print(f"\n{BOLD}╔══ H6 Production Observation Check ══╗{RESET}")
    ts = result.get("checked_at", "")
    print(f"  checked_at : {ts}")
    print()

    print(f"{BOLD}── Strategy State ────────────────────{RESET}")
    print(f"  active_strategy     : {ss.get('active_strategy')}")
    print(f"  shadow_strategy     : {ss.get('shadow_strategy')}")
    print(f"  rollback_status     : {ss.get('rollback_status')}")
    print(f"  rollback_triggered  : {ss.get('rollback_triggered')}")
    print(f"  planner_focus       : {ss.get('planner_focus')}")
    print()

    print(f"{BOLD}── Live Metrics ──────────────────────{RESET}")
    print(f"  live_30p_edge             : {lm.get('live_30p_edge')}")
    print(f"  live_50p_edge             : {lm.get('live_50p_edge')}")
    print(f"  consecutive_negative_30p  : {lm.get('consecutive_negative_30p')}")
    print(f"  current_regime            : {lm.get('current_regime')}")
    print(f"  bear_regime_warning       : {lm.get('bear_regime_warning')}")
    print()

    print(f"{BOLD}── Latest Report ─────────────────────{RESET}")
    print(f"  risk_level          : {lr.get('latest_h6_risk_level')}")
    print(f"  action              : {lr.get('latest_h6_action_recommendation')}")
    print(f"  report_path         : {lr.get('latest_h6_daily_report_path')}")
    if "error" in lr:
        print(f"  [warning]           : {lr['error']}")
    print()

    print(f"{BOLD}── Data Hygiene ──────────────────────{RESET}")
    print(f"  non_numeric_predictions  : {dh.get('non_numeric_predictions_count')}")
    print(f"  non_numeric_outcomes     : {dh.get('non_numeric_outcomes_count')}")
    print(f"  h6_test_cto_backlog      : {dh.get('h6_test_cto_backlog_count')}")
    print(f"  h6_running_rollback_task : {dh.get('h6_running_rollback_task_count')}")
    print()

    print(f"{BOLD}── Scheduler Health ──────────────────{RESET}")
    daemon_icon = "✓" if sh.get("copilot_daemon_running") else "✗"
    print(f"  copilot_daemon_running   : {daemon_icon} ({sh.get('copilot_daemon_status')} pid={sh.get('copilot_daemon_pid')})")
    print(f"  queued_research_>5min    : {sh.get('queued_research_over_5min_count')}")
    print(f"  active_locks             : {sh.get('active_locks_count')}")
    print()

    print(f"{BOLD}══ Observation Verdict ═══════════════{RESET}")
    print(f"  {verdict_color}{BOLD}{verdict}{RESET}")
    if reasons:
        print("  Reasons:")
        for r in reasons:
            print(f"    • {r}")
    else:
        print("  All checks passed.")
    print()


# ────────────────────────────────────────────────────────────────────────────
# Entry point
# ────────────────────────────────────────────────────────────────────────────

def run_check() -> dict:
    conn = _db_conn()
    try:
        strategy_state = _collect_strategy_state(conn)
        live_metrics = _collect_live_metrics(conn)
        data_hygiene = _collect_data_hygiene(conn)
        scheduler_health = _collect_scheduler_health(conn)
    finally:
        conn.close()

    latest_report = _collect_latest_report()

    verdict, reasons = _compute_verdict(
        strategy_state, live_metrics, latest_report, data_hygiene, scheduler_health
    )

    return {
        "checked_at": datetime.now().isoformat(timespec="seconds"),
        "strategy_state": strategy_state,
        "live_metrics": live_metrics,
        "latest_report": latest_report,
        "data_hygiene": data_hygiene,
        "scheduler_health": scheduler_health,
        "observation_verdict": verdict,
        "verdict_reasons": reasons,
    }


def main() -> None:
    parser = argparse.ArgumentParser(
        description="H6 Production Observation Check",
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument(
        "--json", action="store_true", dest="output_json",
        help="Output machine-readable JSON",
    )
    args = parser.parse_args()

    try:
        result = run_check()
    except FileNotFoundError as exc:
        msg = {"error": str(exc), "observation_verdict": "ACTION_REQUIRED"}
        if args.output_json:
            print(json.dumps(msg, ensure_ascii=False, indent=2))
        else:
            print(f"ERROR: {exc}")
        sys.exit(2)

    if args.output_json:
        print(json.dumps(result, ensure_ascii=False, indent=2))
    else:
        _print_human(result)

    sys.exit(_exit_code(result["observation_verdict"]))


if __name__ == "__main__":
    main()
